public class PrintUtil {
    public static void PrintBlankLines(int a) {
        for (int i = 0; i < a; i++) {
            System.out.println("");
        }
    }

    public static void PrintAsterisksLines(int a) {
        for (int i = 0; i < a; i++) {
            System.out.println("***************************");
        }
    }

    public static void PrintAsteriskLines(int a) {
        for (int i = 0; i < a; i++) {
            System.out.println("*");
        }
    }

}
